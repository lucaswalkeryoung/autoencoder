# --------------------------------------------------------------------------------------------------
# ------------------------------- Dataset Batch Sampler and Iterator -------------------------------
# --------------------------------------------------------------------------------------------------
import torch.utils.data as datatools

import random
import typing


# --------------------------------------------------------------------------------------------------
# ------------------------------------ CLASS :: Dataset Manager ------------------------------------
# --------------------------------------------------------------------------------------------------
class Sampler(datatools.Sampler):

    # ------------------------------------------------------------------------------------------
    # ---------------------------------- OPERATOR :: Iterator ----------------------------------
    # ------------------------------------------------------------------------------------------
    def __iter__(self) -> typing.Iterator[int]:
        return iter(random.sample(range(2048), 2048))
